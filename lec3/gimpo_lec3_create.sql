-- 로트 상세 조회 (조인 완료)
CREATE OR REPLACE VIEW v_lot_detail AS
SELECT
    l.lot_id,
    c.category_name                         AS category,
    i.item_code,
    i.item_name,
    l.import_date,
    l.import_qty,
    l.current_stock,
    l.avg_monthly_sales,
    l.stock_months,
    l.next_shipment_date,
    l.next_shipment_qty,
    l.unit_price_usd,
    l.exchange_rate,
    l.selling_price_krw,
    s.status_name                           AS status,
    l.import_qty * l.unit_price_usd * l.exchange_rate   AS import_cost_krw,
    l.current_stock * l.selling_price_krw               AS selling_total_krw,
    i.shelf_life_months,
    EXTRACT(YEAR FROM l.import_date)        AS import_year
FROM fact_import_lot l
JOIN dim_item i         ON l.item_id = i.item_id
JOIN dim_category c     ON i.category_id = c.category_id
JOIN dim_lot_status s   ON l.status_id = s.status_id;

-- 품목별 연도별 요약
CREATE OR REPLACE VIEW v_item_yearly_summary AS
SELECT
    i.item_code,
    i.item_name,
    c.category_name                         AS category,
    EXTRACT(YEAR FROM l.import_date)        AS import_year,
    COUNT(*)                                AS lot_count,
    SUM(l.import_qty)                       AS total_import_qty,
    ROUND(AVG(l.unit_price_usd), 2)         AS avg_unit_price_usd,
    ROUND(AVG(l.exchange_rate))             AS avg_exchange_rate,
    SUM(l.import_qty * l.unit_price_usd * l.exchange_rate)  AS total_import_cost_krw
FROM fact_import_lot l
JOIN dim_item i         ON l.item_id = i.item_id
JOIN dim_category c     ON i.category_id = c.category_id
GROUP BY i.item_code, i.item_name, c.category_name, EXTRACT(YEAR FROM l.import_date);

-- 현재 재고 현황 (보유중만)
CREATE OR REPLACE VIEW v_current_inventory AS
SELECT
    i.item_code,
    i.item_name,
    c.category_name                         AS category,
    l.lot_id,
    l.import_date,
    l.current_stock,
    l.avg_monthly_sales,
    l.stock_months,
    l.next_shipment_date,
    l.next_shipment_qty,
    l.unit_price_usd,
    l.exchange_rate,
    l.selling_price_krw,
    i.shelf_life_months,
    -- 경과 개월
    (EXTRACT(YEAR FROM CURRENT_DATE) - EXTRACT(YEAR FROM l.import_date)) * 12
      + (EXTRACT(MONTH FROM CURRENT_DATE) - EXTRACT(MONTH FROM l.import_date))
                                            AS months_elapsed,
    -- 잔여 유통기한
    i.shelf_life_months - (
      (EXTRACT(YEAR FROM CURRENT_DATE) - EXTRACT(YEAR FROM l.import_date)) * 12
      + (EXTRACT(MONTH FROM CURRENT_DATE) - EXTRACT(MONTH FROM l.import_date))
    )                                       AS remaining_shelf_life
FROM fact_import_lot l
JOIN dim_item i         ON l.item_id = i.item_id
JOIN dim_category c     ON i.category_id = c.category_id
WHERE l.status_id = 1;